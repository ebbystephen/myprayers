            title: (myPrayer.title && prayer.title) ? myPrayer.title + " (" + prayer.title  + ") ": (myPrayer.title || prayer.title || null),
            content: prayer.content || myPrayer.content || null,
            hasCounter: myPrayer.hasCounter,
